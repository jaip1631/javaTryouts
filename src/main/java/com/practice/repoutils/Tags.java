package com.practice.repoutils;

/*
This file just maintains tags for searchability and grouping of programs.
If a program falls into multiple tags, always use tags comma separated
in same order given below.

eg A program containing ArrayTag, BinarySearchTag & LeftRightTag could be
searched as regex: ArrayTag(.*)BinarySearchTag(.*)LeftRightTag only if
tags are entered in same order.
*/

public class Tags {
  /*

  ArrayTag: Array questions

  GreedyTag: Greedy approach

  BinarySearchTag: Binary Search algorithm

  LeftRightTag: In a sorted array, find pair with sum=N.
              Take left and right. Move left -> left++ if curSum<N.
              Move right -> right-- if curSum>N

  MatrixTag: Matrix questions

  DPTag: DP questions

  BackTrackingTag: BackTracking questions

  LinkedListTag: LinkedList questions

  TreeTag: Tree questions
  */
}
